package modelo;

public class Tramite {

	private int id;
	private String nombre;
	private String descripcion;
	private int id_ventanilla;
	private int id_tipoTramite;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getId_ventanilla() {
		return id_ventanilla;
	}
	public void setId_ventanilla(int id_ventanilla) {
		this.id_ventanilla = id_ventanilla;
	}
	public int getId_tipoTramite() {
		return id_tipoTramite;
	}
	public void setId_tipoTramite(int id_tipoTramite) {
		this.id_tipoTramite = id_tipoTramite;
	}
	
	
	
}
